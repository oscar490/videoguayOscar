<?php

namespace app\models;

use Yii;
use yii\imagine\Image;
use yii\web\IdentityInterface;

/**
 * This is the model class for table "usuarios".
 *
 * @property int $id
 * @property string $nombre
 * @property string $password
 * @property string $email
 */
class Usuarios extends \yii\db\ActiveRecord implements IdentityInterface
{
    const ESCENARIO_CREATE = 'create';
    const ESCENARIO_UPDATE = 'update';

    public $passwordRepetir;

    /**
     * Contiene la foto subida en el formulario.
     * @var UploadedFile
     */
    public $foto;
    /**
     * @inheritdoc
     */
    public static function tableName()
    {
        return 'usuarios';
    }

    /**
     * @inheritdoc
     */
    public function rules()
    {
        return [
            [['nombre'], 'required'],
            [['password', 'passwordRepetir'], 'required', 'on' => self::ESCENARIO_CREATE],
            [['nombre', 'password', 'passwordRepetir', 'email'], 'string', 'max' => 255],
            [['email'], 'email'],
            [['nombre'], 'unique'],
            [['email'], 'default'],
            [
                ['passwordRepetir'],
                'compare',
                'compareAttribute' => 'password',
                'skipOnEmpty' => false,
                'on' => [self::ESCENARIO_CREATE, self::ESCENARIO_UPDATE],
            ],
            [['foto'], 'file', 'extensions' => 'jpg, png'],
        ];
    }


    /**
     * Displays a single Usuarios model.
     * @param int $id
     * @return mixed
     * @throws NotFoundHttpException if the model cannot be found
     */
    public function actionView($id)
    {
        return $this->render('view', [
            'model' => $this->findModel($id),
        ]);
    }

    public function upload()
    {
        $nombre = Yii::getAlias('@uploads/') . $this->id . '.png';
        $res = $this->foto->saveAs(
            Yii::getAlias('@uploads/') . $this->id . '.png'
        );

        if ($res) {
            Image::thumbnail($nombre, 80, null)->save($nombre);
        }

        return $res;
    }

    public function attributes()
    {
        return array_merge(parent::attributes(), [
            'passwordRepetir',
            'foto',
        ]);
    }

    public function scenarios()
    {
        return [
            self::ESCENARIO_CREATE => ['nombre', 'passwordRepetir', 'password', 'email'],
            self::ESCENARIO_UPDATE => ['nombre', 'passwordRepetir', 'password', 'email'],
        ];
    }

    public function comprobarIgualdad($attribute, $params)
    {
        if ($this->password !== $this->passwordRepetir) {
            $this->addError($attribute, 'Las contraseñas son diferentes.');
        }
    }

    public function beforeSave($insert)
    {
        if (parent::beforeSave($insert)) {
            if ($this->isNewRecord) {
                $this->auth_key = Yii::$app->security->generateRandomString();
                $this->token_val = Yii::$app->security->generateRandomString();
                if ($this->scenario === self::ESCENARIO_CREATE) {
                    $this->password = Yii::$app->security->generatePasswordHash($this->password);
                }
            } else {
                if ($this->scenario === self::ESCENARIO_UPDATE) {
                    if ($this->password === '') {
                        $this->password = $this->getOldAttribute('password');
                    } else {
                        $this->password = Yii::$app->security->generatePasswordHash($this->password);
                    }
                }
            }
            return true;
        }
        return false;
    }

    /**
     * @inheritdoc
     */
    public function attributeLabels()
    {
        return [
            'id' => 'ID',
            'nombre' => 'Nombre',
            'password' => 'Contraseña',
            'email' => 'Email',
            'passwordRepetir' => 'Contrseña a repetir',
        ];
    }

    /**
     * Finds an identity by the given ID.
     *
     * @param string|int $id the ID to be looked for
     * @return IdentityInterface|null the identity object that matches the given ID.
     */
    public static function findIdentity($id)
    {
        return static::findOne($id);
    }

    /**
     * Finds an identity by the given token.
     *
     * @param string $token the token to be looked for
     * @param null|mixed $type
     * @return IdentityInterface|null the identity object that matches the given token.
     */
    public static function findIdentityByAccessToken($token, $type = null)
    {
    }

    /**
     * @return int|string current user ID
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @return string current user auth key
     */
    public function getAuthKey()
    {
        return $this->auth_key;
    }

    /**
     * @param string $authKey
     * @return bool if auth key is valid for current user
     */
    public function validateAuthKey($authKey)
    {
        return $this->auth_key === $authKey;
    }

    public function validatePassword($password)
    {
        return Yii::$app->security->validatePassword($password, $this->password);
    }
}
